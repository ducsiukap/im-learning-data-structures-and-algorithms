// reverse function
void reversePrint(SinglyLinkedListNode *llist)
{
    SinglyLinkedListNode *current = llist;
    SinglyLinkedListNode *prev(nullptr), *next(nullptr);
    while (current != nullptr)
    {
        next = current->next;
        current->next = prev;
        prev = current;
        current = next;
    }
    // update head
    llist = prev;
    // print list
    printList(llist);
}